numero_um = 2
numero_dois = 3

print("soma:")
print(numero_um + numero_dois)

print("subtração:")
print(numero_um - numero_dois)

print("multiplicação:")
print(numero_um * numero_dois)

print("divisão:")
print(numero_um / numero_dois)

print("módulo:")
print(numero_dois % numero_um)

print("exponenciação:")
print(numero_um ** numero_dois)

print("Divisão inteira:")
print(numero_dois // numero_um)

print("Expressão numérica:")
print((3 + 5) * (7 + 3))